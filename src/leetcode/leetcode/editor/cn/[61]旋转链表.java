/**
 * Definition for singly-linked list.
 * public class ListNode {
 *     int val;
 *     ListNode next;
 *     ListNode() {}
 *     ListNode(int val) { this.val = val; }
 *     ListNode(int val, ListNode next) { this.val = val; this.next = next; }
 * }
 */
class Solution61 {
    public ListNode rotateRight(ListNode head, int k) {
        if (head == null) return null;
        int n = 0;
        ListNode tn = head;
        ListNode tail = head;
        while (tn != null) {
            n++;
            if (tn.next == null) {
                tail = tn;
            }
            tn = tn.next;

        }
        k = n - (k % n);
        if (k == n) return head;
        tail.next = head;
        while (k-- > 0) {
            head = head.next;
            tail = tail.next;
        }
        tail.next = null;
        return head;
    }
}
