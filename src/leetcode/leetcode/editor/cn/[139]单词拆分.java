class Solution139 {
    public boolean wordBreak(String s, List<String> wordDict) {
        int len = s.length();
        boolean[] flags = new boolean[len + 1];
        flags[0] = true;
        for (int i = 1; i <= len; i++) {
            String sub = s.substring(0,i);
            for (String word : wordDict) {
                if (sub.endsWith(word) && flags[sub.length() - word.length()]) {
                    flags[i] = true;
                    break;
                }
            }
        }
        return flags[len];
    }
}
